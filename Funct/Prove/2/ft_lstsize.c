#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		len;
	t_list	*cn;

	len = 0;
	cn = lst;
	while (cn != NULL)
	{
		len++;
		cn = cn->next;
	}
	return (len);
}
