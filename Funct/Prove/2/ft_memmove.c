#include "libft.h"

void	*ft_memmove(void *d, const void *s, size_t n)
{
	char	*a;
	char	*b;

	a = (char *)s;
	b = (char *)d;
	if (b >= a && b < (a + n))
	{
		a += n;
		b += n;
		while (n--)
			*--b = *--a;
	}
	else
	{
		while (n--)
			*b++ = *a++;
	}
	return (d);
}
