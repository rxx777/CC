#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	t_list	*tmp;

	tmp = *lst;
	if (lst == NULL || *lst == NULL)
		return ;
	new->next = tmp;
	*lst = new;
}
