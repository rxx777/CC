#include "libft.h"

int	ft_atoi(const char *a)
{
	int	i;
	int	s;
	int	res;

	i = 0;
	s = 1;
	res = 0;
	while((a[i] > 8 && a[i] < 14) || a[i] == 32)
		i++;
	if(a[i] == '-' || a[i] == '+')
	{
		if(a[i] == '-')
			s *= -1;
		i++;
	}
	while(a[i] > 47 && a[i] < 58)
	{
		res = res*10 + (a[i] - 48);
		i++;
	}
	res *= s;
	return(res);
}
